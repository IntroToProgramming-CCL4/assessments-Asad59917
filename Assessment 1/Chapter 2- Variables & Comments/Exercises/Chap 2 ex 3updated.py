# we will write my name wih whitespace around it 
My_name = "\t \n   John Doe \n \t"

# Now we will print the my name with whitespace 
print("My name with whitespace:")
print(My_name)

# Now we will apply stippring functions to print my name 
print("\nUsing lstrip():")
print(My_name.lstrip())

print("\nUsing rstrip():")
print(My_name.rstrip())

print("\nUsing strip():")
print(My_name.strip())
